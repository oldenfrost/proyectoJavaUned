    private void menuPresionado(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuPresionado

        if(presionado==false){
           opcionMenu.setIcon(iconoMenu);
           opcionMenu.setRolloverIcon(iconoMenuHover);
           esconderMenu(presionado);
           presionado=true;
       }
        else if(presionado){
           opcionMenu.setIcon(iconoFlecha);
           opcionMenu.setRolloverIcon(iconoFlechaHover);
           esconderMenu(presionado);      
           presionado=false;           
        }
        
    }//GEN-LAST:event_menuPresionado

