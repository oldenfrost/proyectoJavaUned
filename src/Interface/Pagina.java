        public void esconderMenu(boolean band){
        if(band==false){
            menu.setPreferredSize(new Dimension(50, menu.getHeight()));
            SwingUtilities.updateComponentTreeUI(this);
        }
        else{
                    menu.setPreferredSize(new Dimension(250, menu.getHeight()));
                    SwingUtilities.updateComponentTreeUI(this);
            }
    }  
