// ACTUALIZA LA PLANILLA
    public void actualizarTablaPlanilla(ArrayList<cEmpleado> planilla) {

        modeloPlanilla.setRowCount(0);
        for (int i = 0; i < planilla.size(); i++) {
            modeloPlanilla.addRow(new Object[]{planilla.get(i).getId(), planilla.get(i).getPlanilla(), planilla.get(i).getMesPlanilla(), planilla.get(i).getAnioPlanilla(), "â¡ " + objDF.format(planillasImpresas.get(i).getSalarioNetoPlanilla())});
        }

    }
   // VERIFICA SI ES UN ENTERO 
    public void verificarEntero(String id) {
        int n = 0;

        try {
            n = Integer.parseInt(id);
            reporteId(n);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "HA INGRESADO UNA:  " + " INCORRECTA; POR FAVOR INGRESE NUMEROS PARA ESTE CAMPO ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }
//REALIZA EL PRIMER REPORTE
    public void reporteId(int id) {
        double salario = 0;

        for (int i = 0; i < planillasImpresas.size(); i++) {
            if (planillasImpresas.get(i).getId() == id) {
                texto += planillasImpresas.get(i).getId() + "  " + planillasImpresas.get(i).getPlanilla() + "  " + planillasImpresas.get(i).getMesPlanilla() + "  " + planillasImpresas.get(i).getAnioPlanilla() + " " +"â¡ " + objDF.format(planillasImpresas.get(i).getSalarioNetoPlanilla()) + "\n";
                salario += planillasImpresas.get(i).getSalarioNetoPlanilla();
            }
        }
        texto += "\n" + "___________________________________________" + "\n" + "SUMA TOTAL SALARIO: " + "â¡ " + objDF.format(salario);

    }
//REALIZA EL SEGUNDO REPORTE
    public void reportePlanilla(String planilla) {
        double salario = 0;
        for (int i = 0; i < planillasImpresas.size(); i++) {
            if (planillasImpresas.get(i).getPlanilla().equalsIgnoreCase(planilla)) {
                texto += planillasImpresas.get(i).getId() + "  " + planillasImpresas.get(i).getPlanilla() + "  " + planillasImpresas.get(i).getMesPlanilla() + "  " + planillasImpresas.get(i).getAnioPlanilla() + " " +"â¡ " + objDF.format(planillasImpresas.get(i).getSalarioNetoPlanilla())  + "\n";
                salario += planillasImpresas.get(i).getSalarioNetoPlanilla();
            }
        }
        texto += "\n" + "___________________________________________" + "\n" + "SUMA TOTAL SALARIO: " + "â¡ " + objDF.format(salario);

    }
